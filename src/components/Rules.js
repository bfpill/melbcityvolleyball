import React from 'react';
import "./Rules.css"
function Rules() {
  return (
    <div>
      <h1>TRIPLES TOURNAMENT RULES</h1>
      <p>Tournament Format: Round robin format with Single Elimination Playoffs.</p>
      <p>Roster: Roster maximum of 4 players</p>
      <p>Players that start a game, must finish that game unless they are injured.</p>
      <p>If a player leaves due to injury, he/she is finished for that match.</p>
      <p>Scoring: All games will be played using rally scoring.</p>
      <p>Tips: No open hand tips.</p>
      <p>Double Contact: Players may double contact any first ball over (including serves) provided the ball is not held.</p>
      <p>Let Serves: Let Serves are NOT legal.</p>
      <p>Blocking: A block is not counted as a team hit.</p>
      <p>Foot Digs: Foot Digs are legal.</p>
      <p>Setting Over: When the ball is set into the opponents court, the setters shoulders must be "Squared Up" or Perpendicular to the line of flight of the ball.</p>
      <p>Time Outs: Each team is allowed two 30 second timeouts per game. Time out may be called prior to server receiving ball on either side of court between plays.</p>
      <p>Side Changes: Teams should switch sides of the court after every 7 points in games to 21 or more, and every 5 points for games to 15.</p>
      <p>Footwear: Players may not wear cleats or spikes of any kind.</p>
    </div>
  );
}

export default Rules;
